    const petOuterContainer = document.getElementById('pet-outer-container');
    const catOption = document.getElementById('cat-option');
    const dogOption = document.getElementById('dog-option');

    let filteredAnimals = animals.filter(animal=> animal.title.includes(searchInput.value));


