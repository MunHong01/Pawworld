let totalElements = animals.length;


function displayPaginationLink(animals){

    totalElements = animals.length;

    const container = document.getElementById('pagination-container');
    container.innerHTML = '';

    if(Math.ceil(animals.length / 6) > 1){
        container.innerHTML += ` <li class="page-item"><a class="page-link" href="#">Previous</a></li>`;
    }
    for(let i = 1;i < Math.ceil(animals.length / 6); i++){
            container.innerHTML += `
            <li class="page-item"><a class="page-link" href="#">${i}</a></li>
                
            `
    }

    if(Math.ceil(animals.length / 6) > 1){
        container.innerHTML += `<li class="page-item"><a class="page-link" href="#">Next</a></li>`;
    }
}

const drop = (arr, n = 1) => arr.slice(n);
const dropLast = (arr, n = 1) => arr.slice(0, -n);

let currentPage = 0;

function nextPage(){

    if(animals.length > 6){
        const slicedAnimal = drop(animals,6);
        const petOuterContainer = document.getElementById('pet-outer-container');
        petOuterContainer.innerHTML = '';
        for(let animal of slicedAnimal){
            petOuterContainer.innerHTML += `
            <div class="col-md-12 col-sm-12 col-lg-4 pt-4">
        <div class="pet-image-container">
            <div class="pet-image">
                <img loading="lazy"
                    srcset="${animal.image}"
                    class="pet-image" />
                <div class="pet-image-text">Adopt ${animal.title}</div>
            </div>
        </div>
    </div>`
        }
        currentPage += 1;
    }
}

